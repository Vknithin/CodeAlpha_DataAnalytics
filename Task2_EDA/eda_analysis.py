# 6. OUTLIER DETECTION
# ===========================================================
def detect_outliers(df: pd.DataFrame) -> None:
    print("\n  6. OUTLIER DETECTION (IQR method)")
    Q1, Q3 = df.price_gbp.quantile([0.25, 0.75])
    IQR = Q3 - Q1
    lower, upper = Q1 - 1.5*IQR, Q3 + 1.5*IQR
    outliers = df[(df.price_gbp < lower) | (df.price_gbp > upper)]
    print(f"  IQR bounds: £{lower:.2f} – £{upper:.2f}")
    print(f"  Price outliers found: {len(outliers)}")

    fig, ax = plt.subplots(figsize=(10, 4))
    ax.boxplot(df.price_gbp, vert=False, patch_artist=True,
               boxprops=dict(facecolor="#AEC6CF"),
               medianprops=dict(color="red", lw=2),
               flierprops=dict(marker="o", markerfacecolor="#e74c3c", ms=5))
    ax.set_xlabel("Price (£)"); ax.set_title("Price Outlier Detection (Box Plot)")
    path = os.path.join(OUTPUT_DIR, "eda_outliers.png")
    plt.tight_layout()
    plt.savefig(path, bbox_inches="tight"); plt.close()
    print(f"  Saved: {path}")



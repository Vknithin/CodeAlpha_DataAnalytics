# 5. BIVARIATE ANALYSIS (plots)
# ===========================================================
def bivariate_analysis(df: pd.DataFrame) -> None:
    print("\n  5. BIVARIATE ANALYSIS  →  saving plots …")

    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("Bivariate Analysis — Books Dataset", fontsize=15, fontweight="bold")

    # Price vs Rating (box)
    ax = axes[0, 0]
    df.boxplot(column="price_gbp", by="star_rating", ax=ax,
               patch_artist=True, notch=True,
               boxprops=dict(facecolor="#74C0FC"),
               medianprops=dict(color="red", lw=2))
    ax.set_xlabel("Star Rating"); ax.set_ylabel("Price (£)")
    ax.set_title("Price by Star Rating")
    plt.sca(ax); plt.title("Price by Star Rating")

    # Average price per genre (horizontal bar)
    ax = axes[0, 1]
    avg_price = df.groupby("genre")["price_gbp"].mean().sort_values()
    avg_price.plot.barh(ax=ax, color=sns.color_palette("Blues_r", len(avg_price)))
    ax.set_xlabel("Average Price (£)"); ax.set_title("Avg Price by Genre")
    for i, v in enumerate(avg_price.values):
        ax.text(v+0.2, i, f"£{v:.1f}", va="center", fontsize=8)

    # Stock vs Rating (stacked bar)
    ax = axes[1, 0]
    stock_rating = df.groupby("star_rating")["in_stock"].value_counts(normalize=True).unstack().fillna(0)
    stock_rating.plot.bar(ax=ax, stacked=True, rot=0,
                          color=["#e74c3c","#2ecc71"], edgecolor="white")
    ax.set_xlabel("Star Rating"); ax.set_ylabel("Proportion")
    ax.set_title("Availability by Star Rating")
    ax.legend(["Out of Stock","In Stock"], loc="lower right")

    # Genre vs avg rating
    ax = axes[1, 1]
    avg_rating = df.groupby("genre")["star_rating"].mean().sort_values(ascending=False)
    avg_rating.plot.bar(ax=ax, color=sns.color_palette(PALETTE, len(avg_rating)), rot=45, edgecolor="white")
    ax.set_xlabel("Genre"); ax.set_ylabel("Avg Star Rating")
    ax.set_title("Avg Rating by Genre"); ax.set_ylim(0, 5)

    plt.tight_layout()
    path = os.path.join(OUTPUT_DIR, "eda_bivariate.png")
    plt.savefig(path, bbox_inches="tight"); plt.close()
    print(f"  Saved: {path}")



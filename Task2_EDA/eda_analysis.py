# 7. HYPOTHESIS TEST
# ===========================================================
def hypothesis_test(df: pd.DataFrame) -> None:
    print("\n  7. HYPOTHESIS TESTING")
    print("  H₀: Mean price of 5-star books == mean price of 1-star books")
    five_star = df[df.star_rating == 5]["price_gbp"]
    one_star  = df[df.star_rating == 1]["price_gbp"]
    t_stat, p_val = stats.ttest_ind(five_star, one_star)
    print(f"  t-statistic : {t_stat:.4f}")
    print(f"  p-value     : {p_val:.4f}")
    conclusion = "REJECT H₀" if p_val < 0.05 else "FAIL TO REJECT H₀"
    print(f"  Conclusion  : {conclusion} (α = 0.05)")
    print(f"  5-star mean : £{five_star.mean():.2f}  |  1-star mean : £{one_star.mean():.2f}")



# 4. UNIVARIATE ANALYSIS (plots)
# ===========================================================
def univariate_analysis(df: pd.DataFrame) -> None:
    print("\n" + "="*60)
    print("  4. UNIVARIATE ANALYSIS  →  saving plots …")
    print("="*60)

    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("Univariate Analysis — Books Dataset", fontsize=15, fontweight="bold")

    # Price histogram
    ax = axes[0, 0]
    df.price_gbp.plot.hist(bins=30, ax=ax, color="#4C72B0", edgecolor="white")
    ax.axvline(df.price_gbp.mean(),  color="red",    ls="--", label=f"Mean  £{df.price_gbp.mean():.2f}")
    ax.axvline(df.price_gbp.median(),color="orange", ls="--", label=f"Median £{df.price_gbp.median():.2f}")
    ax.set_xlabel("Price (£)"); ax.set_ylabel("Count")
    ax.set_title("Price Distribution"); ax.legend()

    # Star rating bar chart
    ax = axes[0, 1]
    rating_counts = df.star_rating.value_counts().sort_index()
    bars = ax.bar(rating_counts.index, rating_counts.values,
                  color=sns.color_palette("YlOrRd", 5), edgecolor="white")
    for bar in bars:
        ax.text(bar.get_x()+bar.get_width()/2, bar.get_height()+3,
                str(int(bar.get_height())), ha="center", fontsize=9)
    ax.set_xlabel("Star Rating"); ax.set_ylabel("Number of Books")
    ax.set_title("Star Rating Distribution"); ax.set_xticks([1,2,3,4,5])

    # Genre pie chart
    ax = axes[1, 0]
    genre_counts = df.genre.value_counts()
    ax.pie(genre_counts.values, labels=genre_counts.index,
           autopct="%1.1f%%", startangle=140,
           colors=sns.color_palette(PALETTE, len(genre_counts)))
    ax.set_title("Books by Genre")

    # Availability bar
    ax = axes[1, 1]
    avail_counts = df.availability.value_counts()
    colors = ["#2ecc71" if "In" in v else "#e74c3c" for v in avail_counts.index]
    avail_counts.plot.bar(ax=ax, color=colors, edgecolor="white", rot=0)
    for i, v in enumerate(avail_counts.values):
        ax.text(i, v+5, str(v), ha="center", fontsize=10)
    ax.set_title("Availability Status"); ax.set_ylabel("Count")

    plt.tight_layout()
    path = os.path.join(OUTPUT_DIR, "eda_univariate.png")
    plt.savefig(path, bbox_inches="tight"); plt.close()
    print(f"  Saved: {path}")



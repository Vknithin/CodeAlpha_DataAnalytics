# ===========================================================
# 1. LOAD & INSPECT
# ===========================================================
def load_and_inspect(path: str) -> pd.DataFrame:
    print("\n" + "="*60)
    print("  1. LOADING & INSPECTING THE DATASET")
    print("="*60)
    df = pd.read_csv(path, index_col="book_id")
    print(f"\n  Shape            : {df.shape[0]} rows × {df.shape[1]} columns")
    print(f"\n  Columns          : {list(df.columns)}")
    print(f"\n  Data types:\n{df.dtypes.to_string()}")
    print(f"\n  Null values:\n{df.isnull().sum().to_string()}")
    print(f"\n  First 5 rows:\n{df.head().to_string()}")
    return df


# ===========================================================
# 2. CLEAN
# ===========================================================
def clean(df: pd.DataFrame) -> pd.DataFrame:
    print("\n" + "="*60)
    print("  2. DATA CLEANING")
    print("="*60)
    before = len(df)
    df = df.drop_duplicates(subset=["title"])
    df["availability"] = df["availability"].str.strip()
    df["in_stock"]     = (df["availability"] == "In stock").astype(int)
    print(f"  Duplicates removed : {before - len(df)}")
    print(f"  Remaining rows     : {len(df)}")
    print(f"  New column 'in_stock' (0/1) added.")
    return df


# ===========================================================
# 3. DESCRIPTIVE STATISTICS
# ===========================================================
def describe(df: pd.DataFrame) -> None:
    print("\n" + "="*60)
    print("  3. DESCRIPTIVE STATISTICS")
    print("="*60)
    print(df[["price_gbp","star_rating"]].describe().round(2).to_string())

    # Additional stats
    print(f"\n  Skewness  (price) : {df.price_gbp.skew():.3f}")
    print(f"  Kurtosis  (price) : {df.price_gbp.kurt():.3f}")

    # Genre breakdown
    print(f"\n  Books per genre:\n{df.genre.value_counts().to_string()}")

    # Rating breakdown
    print(f"\n  Books per star rating:")
    stars = {1:"★☆☆☆☆",2:"★★☆☆☆",3:"★★★☆☆",4:"★★★★☆",5:"★★★★★"}
    for k, v in df.star_rating.value_counts().sort_index().items():
        print(f"    {stars[k]}  {v} books")


